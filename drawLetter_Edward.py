def draw_E(the_turtle):

  the_turtle.forward(100)

  the_turtle.setpos(0,0)

  the_turtle.right(90)

  the_turtle.forward(200)

  the_turtle.penup()

  the_turtle.setpos(0,-100)

  the_turtle.left(90)

  the_turtle.pendown()

  the_turtle.forward(50)

  the_turtle.penup()
  
  the_turtle.setpos(0,-200)

  the_turtle.pendown()

  the_turtle.forward(100)

  

